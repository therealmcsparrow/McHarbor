package i18n

import "fmt"

// translations holds all message translations indexed by language then code.
var translations = map[Lang]map[MsgCode]string{
	EN: messagesEN,
	NL: messagesNL,
	DE: messagesDE,
	ES: messagesES,
	FR: messagesFR,
	PT: messagesPT,
	ZH: messagesZH,
}

// T returns the translated message for the given language and code.
// Falls back to English if the language or code is not found.
func T(lang Lang, code MsgCode) string {
	if msgs, ok := translations[lang]; ok {
		if msg, ok := msgs[code]; ok {
			return msg
		}
	}
	// Fallback to English
	if msg, ok := messagesEN[code]; ok {
		return msg
	}
	return string(code)
}

// Tf returns a formatted translated message with arguments.
func Tf(lang Lang, code MsgCode, args ...any) string {
	return fmt.Sprintf(T(lang, code), args...)
}

